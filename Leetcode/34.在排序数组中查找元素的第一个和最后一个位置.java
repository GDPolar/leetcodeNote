// @lc code=start
class Solution {
    public int[] searchRange(int[] nums, int target) {
        int[] res = new int[2];
        int l = 0, r = nums.length - 1;
        int mid = -1;
        while (l <= r) {
            mid = (l + r) / 2;
            if (nums[mid] < target) {
                l = mid + 1;
            } else if (nums[mid] > target) {
                r = mid - 1;
            } else {
                break;
            }
        }
        // 不存在
        if (l > r) {
            return new int[]{-1, -1};
        }
        res[0] = bsl(nums, 0, mid, target);
        res[1] = bsr(nums, mid, nums.length - 1, target);
        return res;
    }

    // 找第一个比 target 小的数的位置
    public int bsl(int[] nums, int l, int r, int target) {
        while (l <= r) {            
            int mid = (l + r) / 2;
            if (nums[mid] < target) {
                l = mid + 1;
            } else {
                r = mid - 1;
            }
        }
        return r + 1;
    }

    // 找第一个比 target 大的数的位置
    public int bsr(int[] nums, int l, int r, int target) {
        while (l <= r) {            
            int mid = (l + r) / 2;
            if (nums[mid] <= target) {
                l = mid + 1;
            } else {
                r = mid - 1;
            }
        }
        return l - 1;
    }
}
